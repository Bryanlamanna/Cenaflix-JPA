    private void BtEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtEditarActionPerformed

    try{
        TelaEditarPodcast telaEditar = new TelaEditarPodcast();
        
        int linha = TabPodcast.getSelectedRow();
        
        telaEditar.CpProdutor.setText(TabPodcast.getValueAt(linha, 1).toString());
        telaEditar.CpNome.setText(TabPodcast.getValueAt(linha, 2).toString());
        telaEditar.CpNumero.setText(TabPodcast.getValueAt(linha, 3).toString());
        telaEditar.CpDuracao.setText(TabPodcast.getValueAt(linha, 4).toString());
        telaEditar.CpUrl.setText(TabPodcast.getValueAt(linha, 5).toString());
        
        telaEditar.setVisible(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Selecione um registtro!");
        }
    }//GEN-LAST:event_BtEditarActionPerformed

    private void BtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtExcluirActionPerformed
    try{
        int linha = TabPodcast.getSelectedRow();
        String idString = String.valueOf(TabPodcast.getValueAt(linha, 0));
        int id = Integer.parseInt(idString);

        PodcastDAO.excluir(id);
        
        limpaTabela();
        TabelaManager.popularTabela(TabPodcast);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Selecione um registtro!");
        }
    }//GEN-LAST:event_BtExcluirActionPerformed

