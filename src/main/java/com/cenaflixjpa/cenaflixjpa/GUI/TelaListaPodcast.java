   public void setAcesso(int priv){
        if (priv == 3){
            JOptionPane.showMessageDialog(null, "Olá Usuário!\n sua permissão é de USUÁRIO.\n Seja bem-vindo");
            BtCadastrar.setEnabled(false);
            BtEditar.setEnabled(false);
            BtExcluir.setEnabled(false);
        } if (priv == 2) {
            BtEditar.setEnabled(false);
            BtExcluir.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Olá Operador!\n sua permissão é de OPERADOR.\n Seja bem-vindo");
        } else {
            JOptionPane.showMessageDialog(null, "Olá Administrador!\n sua permissão é de ADMINISTRADOR.\n Seja bem-vindo");
            return;
        }
    } 
    
   public void limpaTabela(){
        int rowCount = TabPodcast.getModel().getRowCount();
        
        for (int e = 0; e < rowCount; e++){
            TabPodcast.setValueAt(null, e, 0);
            TabPodcast.setValueAt(null, e, 1);
            TabPodcast.setValueAt(null, e, 2);
            TabPodcast.setValueAt(null, e, 3);
            TabPodcast.setValueAt(null, e, 4);
            TabPodcast.setValueAt(null, e, 5);
        }
                
    }

